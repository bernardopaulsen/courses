s1 = {10,20,30}
s2 = {20,30,40}

# equality
print(s1 == s2)
print(s1 != s2)

# relational
print(s1 > s2)
print(s1 < s2)
print(s1 <= s2)
print(s2 >= s2)

# membership
print(10 in s1)
print(50 in s2)

